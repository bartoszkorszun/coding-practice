class CodersLab {
    public static void main(String[] args) {
        int i = 5;
        char c = 'c';
        long l = 5l;
        double d = 5.0;
        boolean b = true;
        
        System.out.println("Integer: " + i);
        System.out.println("Char: " + c);
        System.out.println("Long: " + l);
        System.out.println("Double: " + d);
        System.out.println("Boolean: " + b);
    }
}